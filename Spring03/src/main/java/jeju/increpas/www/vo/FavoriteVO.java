package jeju.increpas.www.vo;

public class FavoriteVO {
	private int fno, mno, ano, atno;

	public int getFno() {
		return fno;
	}

	public void setFno(int fno) {
		this.fno = fno;
	}

	public int getMno() {
		return mno;
	}

	public void setMno(int mno) {
		this.mno = mno;
	}

	public int getAno() {
		return ano;
	}

	public void setAno(int ano) {
		this.ano = ano;
	}
	
	public int getAtno() {
		return atno;
	}
	
	public void setAtno(int atno) {
		this.atno = atno;
	}
}
