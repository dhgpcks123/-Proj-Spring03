package jeju.increpas.www.vo;

public class AreaInfoVO {
	private int ano, atno, apno, ahit, ax, ay;
	private String aname, aloc, atel, abody;
	
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public int getAtno() {
		return atno;
	}
	public void setAtno(int atno) {
		this.atno = atno;
	}
	public int getApno() {
		return apno;
	}
	public void setApno(int apno) {
		this.apno = apno;
	}
	public int getAhit() {
		return ahit;
	}
	public void setAhit(int ahit) {
		this.ahit = ahit;
	}
	public int getAx() {
		return ax;
	}
	public void setAx(int ax) {
		this.ax = ax;
	}
	public int getAy() {
		return ay;
	}
	public void setAy(int ay) {
		this.ay = ay;
	}
	public String getAname() {
		return aname;
	}
	public void setAname(String aname) {
		this.aname = aname;
	}
	public String getAloc() {
		return aloc;
	}
	public void setAloc(String aloc) {
		this.aloc = aloc;
	}
	public String getAtel() {
		return atel;
	}
	public void setAtel(String atel) {
		this.atel = atel;
	}
	public String getAbody() {
		return abody;
	}
	public void setAbody(String abody) {
		this.abody = abody;
	}
}
